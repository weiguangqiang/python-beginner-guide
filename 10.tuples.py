# tuple [元组]

'''A tuple in Python is a collection that cannot be modified.
A tuple is defined using parenthesis[圆括号].'''

# A tuple with one item ( a comma is needed in the end )
x = (1,)

# multiple items
y = (1,2,3,4)

print(x[0])

print(y[2])

# To print the last element, you can count from the back using the minus[负号] sign.
print(y[-1])
